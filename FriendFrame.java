    private void changePassword_jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePassword_jButtonActionPerformed
        // TODO add your handling code here:
        int flag=-1;
        
        JPasswordField field1 = new JPasswordField(10);
        JPasswordField field2 = new JPasswordField(10);
      
        Object[] fields = {
            "Old Password", field1,
            "New Password", field2
        };
        int value = JOptionPane.showConfirmDialog(frame, fields, "Enter text", JOptionPane.OK_CANCEL_OPTION);
        if (value == JOptionPane.OK_OPTION)
        {
            // OK was pressed
            String oldPassword = field1.getText();
            char[] newPassword = field2.getPassword();
            // handle them
            flag = DB_Project.changePassword(oldPassword, newPassword);
        }
        
        if(flag==1) JOptionPane.showMessageDialog(null,"Password change was Successful.","Confirmation",JOptionPane.INFORMATION_MESSAGE);
        else        JOptionPane.showMessageDialog(null,"Password change was Unsuccessful.","Confirmation",JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_changePassword_jButtonActionPerformed

