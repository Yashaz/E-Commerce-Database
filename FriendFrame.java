    private void addFriendItems(ArrayList<Integer> friends) throws SQLException
    
    {
        int i; ResultSet res;
             
        DefaultTableModel tb=(DefaultTableModel) jTable6.getModel();
        
              for(i=0;i<friends.size();i++)
            {  
                res = DB_Project.returnFriendDetails(friends.get(i));
                
                while(res.next())
                {
                    items1[0]=res.getInt("custid");
                    items1[1]=res.getString("custname");
                    items1[2]=res.getString("username");
                            
                    tb.addRow(items1);    
                 }
            }      
        
    }// end of addFriendItems
    
    public void setMyReviewsDetails(ArrayList<Integer> tids) throws SQLException{
        
         int i; ResultSet res;
             
        DefaultTableModel tb=(DefaultTableModel) jTable8.getModel();
        
              for(i=0;i<tids.size();i=i+2)
            {  
                res = DB_Project.returnReviews(tids.get(i), tids.get(i+1));
                
                while(res.next())
                {
                    items3[0]=res.getInt("tid");
                    items3[0]=res.getInt("pid");
                    items3[1]=res.getString("modelname");
                    items3[2]=res.getString("prodReview");
                            
                    tb.addRow(items3);    
                 }
            }     
        
        
    }// end of setMyReviewsDetails

